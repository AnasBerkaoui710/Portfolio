/* --- STATE --- */
let currentLang = localStorage.getItem('selected-lang') || 'fr';
let currentTheme = localStorage.getItem('selected-theme') || 'dark'; // Default to dark for tech feel

/* --- SELECTORS --- */
const navMenu = document.getElementById('nav-menu'),
    navToggle = document.getElementById('nav-toggle'),
    navClose = document.getElementById('nav-close'),
    navLinks = document.querySelectorAll('.nav__link'),
    themeButton = document.getElementById('theme-button'),
    langDropdownBtn = document.getElementById('lang-dropdown-btn'),
    langDropdownMenu = document.getElementById('lang-dropdown-menu'),
    currentLangFlag = document.getElementById('current-lang-flag'),
    langBtns = document.querySelectorAll('.lang-dropdown__item'),
    header = document.getElementById('header'),
    contactForm = document.getElementById('contact-form'),
    formResponse = document.getElementById('form-response'),
    scrollUp = document.getElementById('scroll-up'),
    blogModal = document.getElementById('blog-modal'),
    modalClose = document.getElementById('modal-close'),
    modalTitle = document.getElementById('modal-title'),
    modalBody = document.getElementById('modal-body'),
    heroSubtitle = document.getElementById('hero-subtitle');

/* --- THEME TOGGLE --- */
const applyTheme = (theme) => {
    document.documentElement.setAttribute('data-theme', theme);
    if (theme === 'dark') {
        themeButton.classList.replace('fa-moon', 'fa-sun');
    } else {
        themeButton.classList.replace('fa-sun', 'fa-moon');
    }
};

const toggleTheme = () => {
    currentTheme = currentTheme === 'light' ? 'dark' : 'light';
    applyTheme(currentTheme);
    localStorage.setItem('selected-theme', currentTheme);
};

// Initial apply
applyTheme(currentTheme);
themeButton.addEventListener('click', toggleTheme);

/* --- LANGUAGE TOGGLE --- */
// Toggle dropdown visibility
if(langDropdownBtn) {
    langDropdownBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdownMenu.classList.toggle('show');
    });
}

// Close dropdown when clicking outside
document.addEventListener('click', (e) => {
    if(langDropdownMenu && langDropdownMenu.classList.contains('show') && !e.target.closest('#lang-dropdown')) {
        langDropdownMenu.classList.remove('show');
    }
});

const updateLanguage = (lang) => {
    currentLang = lang;
    localStorage.setItem('selected-lang', lang);
    
    // Update active class on dropdown items
    langBtns.forEach(btn => btn.classList.toggle('active', btn.getAttribute('data-lang') === lang));
    
    // Update main button flag
    if(currentLangFlag) {
        currentLangFlag.src = lang === 'fr' ? 'https://flagcdn.com/w20/fr.png' : 'https://flagcdn.com/w20/gb.png';
        currentLangFlag.alt = lang.toUpperCase();
    }

    // Close dropdown
    if(langDropdownMenu) langDropdownMenu.classList.remove('show');

    document.documentElement.setAttribute('lang', lang);
    if(heroSubtitle) heroSubtitle.textContent = lang === 'fr' ? CONFIG.title_fr : CONFIG.title_en;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = translations[lang][key];
            else el.innerHTML = translations[lang][key];
        }
    });

    const footerCopy = document.getElementById('footer-copy');
    if(footerCopy) footerCopy.innerHTML = `&#169; ${new Date().getFullYear()} ${CONFIG.name}. ${lang === 'fr' ? 'Tous droits réservés.' : 'All rights reserved.'}`;
};

langBtns.forEach(btn => btn.addEventListener('click', () => updateLanguage(btn.getAttribute('data-lang'))));
updateLanguage(currentLang);

/* --- MOBILE MENU --- */
if (navToggle) navToggle.addEventListener('click', () => navMenu.classList.add('show-menu'));
if (navClose) navClose.addEventListener('click', () => navMenu.classList.remove('show-menu'));
navLinks.forEach(link => link.addEventListener('click', () => navMenu.classList.remove('show-menu')));

/* --- HEADER SHADOW --- */
window.addEventListener('scroll', () => {
    if (window.scrollY >= 50) header.style.boxShadow = '0 4px 12px rgba(0,0,0,0.1)';
    else header.style.boxShadow = 'none';
});

/* --- ACTIVE LINK ON SCROLL --- */
window.addEventListener('scroll', () => {
    const scrollY = window.pageYOffset;
    document.querySelectorAll('section[id]').forEach(current => {
        const sectionHeight = current.offsetHeight,
            sectionTop = current.offsetTop - 58,
            sectionId = current.getAttribute('id');

        const navLink = document.querySelector('.nav__menu a[href*=' + sectionId + ']');
        if (navLink) {
            if (scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
                navLink.classList.add('active-link');
            } else {
                navLink.classList.remove('active-link');
            }
        }
    });
});

/* --- SCROLL UP --- */
window.addEventListener('scroll', () => {
    if (window.scrollY >= 350) scrollUp.classList.add('show-scroll');
    else scrollUp.classList.remove('show-scroll');
});

/* --- TYPING ANIMATION --- */
const typeEffect = (element, text, speed = 80) => {
    element.innerHTML = "";
    let i = 0;
    const timer = setInterval(() => {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
        } else {
            clearInterval(timer);
        }
    }, speed);
};

window.addEventListener('load', () => {
    const titleEl = document.getElementById('hero-title');
    if (titleEl) typeEffect(titleEl, CONFIG.name, 100);
});

/* --- STAGGERED REVEAL --- */
const staggeredObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry, index) => {
        if (entry.isIntersecting) {
            setTimeout(() => {
                entry.target.classList.add('visible');
            }, index * 50);
        }
    });
}, { threshold: 0.1 });

document.querySelectorAll('section, .skill__card, .project__card, .blog__card').forEach(el => {
    el.classList.add('fade-up');
    staggeredObserver.observe(el);
});

/* --- CONTACT FORM --- */
contactForm.addEventListener('submit', (e) => {
    e.preventDefault();
    formResponse.textContent = translations[currentLang].form_success;
    formResponse.style.color = "var(--primary-color)";
    contactForm.reset();
    setTimeout(() => formResponse.textContent = "", 5000);
});

/* --- RESEARCH MODAL --- */
window.openModal = (id) => {
    const content = researchContent[currentLang][id];
    modalTitle.textContent = content.title;
    modalBody.innerHTML = content.body;
    blogModal.classList.add('active-modal');
    document.body.style.overflow = 'hidden';
};

modalClose.addEventListener('click', () => {
    blogModal.classList.remove('active-modal');
    document.body.style.overflow = 'auto';
});
